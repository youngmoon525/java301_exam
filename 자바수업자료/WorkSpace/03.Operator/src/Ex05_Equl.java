
public class Ex05_Equl {
	public static void main(String[] args) {
		//복합 대입 연산자 += , -= , *= , /=
		int result = 0 ; //0으로 초기화
		result += 10;//<= result = result(0) + 10 ;
		System.out.println(result);
		result -= 3;//<= result = result(10) - 3 ;
		System.out.println(result);
		result *= 2;//<= result = result(7) * 2 ;
		System.out.println(result);
		result /= 3;//<= result = result(14) / 3 ;
		System.out.println(result);
	}
}
