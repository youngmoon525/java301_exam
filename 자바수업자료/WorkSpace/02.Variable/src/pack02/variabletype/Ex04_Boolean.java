package pack02.variabletype;

public class Ex04_Boolean {
	public static void main(String[] args) {
		//부울형(논리형) : true , false
		//                1  ,   0
		//true 또는 false의 데이터만 저장이 가능한 변수타입
		//개발자가 조건식을 만들었을때 그 조건식의 결과가 참(맞는지)
		//거짓(맞지않는지)를 체크하고 또 그결과를 얻어오기 위함
		//ex) 1+1 == 2 (true) 1+1 == 10 (false)
		boolean t = true;//true , false 자바 예약어
		boolean f = false;
		//
		System.out.println("boolean t 의 값 : " + t);
		System.out.println("boolean f 의 값 : " + f);
	}
}
